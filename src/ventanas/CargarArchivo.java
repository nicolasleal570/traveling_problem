    /**
     * Cargar un nuevo archivo
     */
    private void btnCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarActionPerformed
        if (lienzo.getSeleccionado().showDialog(this, "Abrir Archivo") == JFileChooser.APPROVE_OPTION) {

            lienzo.setArchivo(lienzo.getSeleccionado().getSelectedFile()); 
            String content = FileLoader.cargarArchivoTxt(lienzo.getArchivo().toString()); // Txt content
            this.txtContent.setText(content);
            guardado = false;
        }
    }//GEN-LAST:event_btnCargarActionPerformed

    /**
     * Guarda el archivo de texto modificado por el Text Area 
     */
    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (FileLoader.guardarArchivoTxt(lienzo.getArchivo(), this.txtContent.getText())) {
            
            JOptionPane.showMessageDialog(null, "Archivo Guardado Satisfactoriamente!");
            
            // Establece los nuevos valores para dibujar el nuevo grafo guardado
            String[] categoria = FileLoader.cargarArchivoTxt(lienzo.getArchivo().toString()).split("-");
            lienzo.setCiudades(FileLoader.cargarCiudadesTxt(categoria[1]));
            lienzo.setGraph(FileLoader.cargarEnlacesTxt(categoria[2], lienzo.getCiudades().size(), lienzo.getGraph()));
            
            guardado = true;
            
//            System.out.println(lienzo.getGraph());
            
        }
        
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnSalirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalirActionPerformed
        
        if (guardado) {
            this.setVisible(false);
        } else {
            int opt = JOptionPane.showConfirmDialog(null, "Debe guardar los cambios realizados antes de cerrar esta ventana sino se perder√° todo\n"
                    + "Cerrar de todos modos?");
            
            if (opt == 0) {
                guardado = true;
                this.setVisible(false);
            } else {
                guardado = false;
            }
            
        }
        
    }//GEN-LAST:event_btnSalirActionPerformed

