    private void btnAgregarCiudadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarCiudadActionPerformed

        if (this.txtNombreCiudad.equals("") || this.txtDistanciaCiudad1.equals("") || this.txtDistanciaCiudad2.equals("")
                || (this.selectorCiudadAdy1.getSelectedIndex() == this.selectorCiudadAdy2.getSelectedIndex()) 
                || (this.selectorCiudadAdy2.getSelectedIndex() == this.selectorCiudadAdy1.getSelectedIndex()))  {
            JOptionPane.showMessageDialog(null, "Completar todos los campos.\n "
                    + "Las ciudades adyacentes deben de ser distintas.");
        } else {
            
            int idCiudad = lienzo.getCiudades().size() - 1;
            Ciudad city = new Ciudad(idCiudad, this.txtNombreCiudad.getText());
            lienzo.getCiudades().add(city);
            
            lienzo.getGraph().cargarNuevaCiudad(lienzo.getCiudades().size(), lienzo.getGraph().getGraphEstructura());
            
            this.setVisible(false);            
        }
        
    }//GEN-LAST:event_btnAgregarCiudadActionPerformed

