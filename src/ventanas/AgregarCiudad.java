    /**
     * Inicia los valores de variables extras del sistema
     */
    private void initMoreConfig() {

        for (Ciudad c : lienzo.getCiudades()) {
            String txt = "Id: " + c.getId() + ", " + c.getNombre();
            this.selectorCiudadAdy1.addItem(txt);
            this.selectorCiudadAdy2.addItem(txt);
        }

        this.selectorCiudadAdy2.setSelectedIndex(1);

    }

